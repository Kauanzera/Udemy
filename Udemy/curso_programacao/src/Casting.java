//	CASTING: É o ato de transformar um Tipo de variável em outro. == Int -> Float || Double -> Int.
public class Casting {

	public static void main(String[] args) {
			
		int a, b;
		double resultado;
			
		a = 5;
		b = 2;
					
		resultado = (double) a / b;
//		  #CASTING   ^^^^^^	
		
		System.out.print(resultado);		

		
	}
}
